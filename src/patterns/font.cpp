int getFontIndex(char c) {
  if (c >= 'A' && c <= 'Z')
    return c - 'A';
  if (c >= 'a' && c <= 'z')
    return c - 'a';
  if (c >= '0' && c <= '9')
    return 26 + (c - '0');
  if (c == ' ')
    return 36;
  if (c == '!')
    return 37;
  if (c == '.')
    return 38;
  if (c == '-')
    return 39;
  if (c == '<')
    return 40; // heart
  if (c == '>')
    return 41; // Christmas tree
  if (c == ':')
    return 42; // colon
  return 36;
}
